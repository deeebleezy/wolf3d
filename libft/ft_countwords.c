#include "libft.h"

int		ft_countwords(char *s, char c)
{
	int		words;

	while (*s && *s == c)
		s++;
	words = ((*s) ? 1 : 0);
	while (*s)
	{
		if (*s == c && s[1] && s[1] != c)
			words++;
		s++;
	}
	return (words);
}
